// EQUAL
console.log('3'==3); // true - compares value but not type

/*
    - coercion is the process of converting a value from one type to another
*/

// STRICT EQUAL
console.log(3 === 3);
console.log('3'=== 3);

// NOT EQUAL
console.log('3' != 3);

// STRICT NOT EQUAL
console.log('3' !== 3);

// GREATER THAN
console.log(3 > 2);

// LESS THAN
console.log(2 < 3);

// GREATER THAN OR EQUAL TO
console.log(3 >= 2);
console.log(2 >= 2);

// LESS THAN OR EQUAL TO
console.log(2 <= 3);
console.log(2 <= 2);

// AND
2 && 3
console.log(2 > 3 && 4 < 5); // false - both are not true

// OR
2 || 3
console.log(2 > 3 || 4 < 5);  // true - one of them (4 < 5) is true

